#!/bin/bash

# Первое диалоговое окно
gdialog --title "Первый диалог" --yesno "Вы хотите открыть второе диалоговое окно?" 10 30

# Проверяем результат первого диалогового окна
if [ $? -eq 0 ]; then
  # Если выбрано "Yes", открываем второе диалоговое окно
  gdialog --title "Второй диалог" --yesno "Вы хотите продолжить?" 10 30
  
  # Проверяем результат второго диалогового окна
  if [ $? -eq 0 ]; then
    # Если выбрано "Yes" во втором диалоговом окне, выводим сообщение
    gdialog --title "Сообщение" --msgbox "Вы выбрали продолжить." 10 30
  else
    # Если выбрано "No" во втором диалоговом окне, выводим сообщение
    gdialog --title "Сообщение" --msgbox "Вы выбрали отменить." 10 30
  fi
else
  # Если выбрано "No" в первом диалоговом окне, выводим сообщение
  gdialog --title "Сообщение" --msgbox "Вы выбрали отменить." 10 30
fi

